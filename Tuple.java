package me.turing.machine.turing_machine;

// A quick class to make handling the machine instructions easier
public class Tuple<A,B,C,D,E> {
	public final A a; 
	public final B b; 
	public final C c; 
	public final D d; 
	public final E e; 
	
	public Tuple(A a, B b, C c, D d, E e) { 
		this.a = a; 
		this.b = b; 
		this.c = c; 
		this.d = d; 
		this.e = e; 
	} 
	
}